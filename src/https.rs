// Miyoos have no RTC and boot with a bogus clock (e.g. 1971), which causes rustls to
// reject certs as "not yet valid". We wrap the real verifier and clamp "now" to a
// build-time constant so time-based validity checks always pass.
#[derive(Debug)]
struct TimelessVerifier(Arc<dyn rustls::client::danger::ServerCertVerifier>);

impl rustls::client::danger::ServerCertVerifier for TimelessVerifier {
    fn verify_server_cert(
        &self,
        end_entity: &rustls::pki_types::CertificateDer<'_>,
        intermediates: &[rustls::pki_types::CertificateDer<'_>],
        server_name: &rustls::pki_types::ServerName<'_>,
        ocsp_response: &[u8],
        _now: rustls::pki_types::UnixTime,
    ) -> Result<rustls::client::danger::ServerCertVerified, rustls::Error> {
        // Use a fixed time well within our cert's validity window instead of the system clock.
        // Cert valid: Jun 22 2026 – Sep 20 2026. Pick Aug 1 2026 (1753920000).
        let now = rustls::pki_types::UnixTime::since_unix_epoch(
            std::time::Duration::from_secs(1753920000)
        );
        self.0.verify_server_cert(end_entity, intermediates, server_name, ocsp_response, now)
    }
    fn verify_tls12_signature(&self, msg: &[u8], cert: &rustls::pki_types::CertificateDer<'_>, sig: &rustls::DigitallySignedStruct) -> Result<rustls::client::danger::HandshakeSignatureValid, rustls::Error> {
        self.0.verify_tls12_signature(msg, cert, sig)
    }
    fn verify_tls13_signature(&self, msg: &[u8], cert: &rustls::pki_types::CertificateDer<'_>, sig: &rustls::DigitallySignedStruct) -> Result<rustls::client::danger::HandshakeSignatureValid, rustls::Error> {
        self.0.verify_tls13_signature(msg, cert, sig)
    }
    fn supported_verify_schemes(&self) -> Vec<rustls::SignatureScheme> {
        self.0.supported_verify_schemes()
    }
}

pub fn make_tls_config() -> Arc<ClientConfig> {
    let mut root_store = rustls::RootCertStore {
        roots: webpki_roots::TLS_SERVER_ROOTS.to_vec(),
    };
    // ISRG Root YR is not yet in webpki-roots; add it manually via its cross-signed cert.
    let _ = root_store.add(rustls::pki_types::CertificateDer::from(ISRG_ROOT_YR_DER));
    let verifier = rustls::client::WebPkiServerVerifier::builder(Arc::new(root_store))
        .build()
        .expect("verifier");
    Arc::new(
        ClientConfig::builder()
            .dangerous()
            .with_custom_certificate_verifier(Arc::new(TimelessVerifier(verifier)))
            .with_no_client_auth(),
    )
}

fn tls_roundtrip(host: &str, request: &[u8], connect_timeout: u64, read_timeout: u64) -> Result<Vec<u8>, String> {
    let addr = (host, 443u16)
        .to_socket_addrs()
        .map_err(|e| e.to_string())?
        .next()
        .ok_or("no addr")?;

    let tcp = TcpStream::connect_timeout(&addr, Duration::from_secs(connect_timeout))
        .map_err(|e| e.to_string())?;
    tcp.set_read_timeout(Some(Duration::from_secs(read_timeout))).ok();

    let config = make_tls_config();
    let server_name: ServerName<'static> = ServerName::try_from(host.to_string())
        .map_err(|e| e.to_string())?;
    let mut conn = rustls::ClientConnection::new(config, server_name)
        .map_err(|e| e.to_string())?;
    let mut tcp = tcp;
    let mut stream = rustls::Stream::new(&mut conn, &mut tcp);

    stream.write_all(request).map_err(|e| e.to_string())?;

    let mut resp = Vec::new();
    let _ = stream.read_to_end(&mut resp);
    if resp.is_empty() { return Err("empty response".into()); }

    let body_start = resp.windows(4).position(|w| w == b"\r\n\r\n")
        .map(|i| i + 4)
        .or_else(|| resp.windows(2).position(|w| w == b"\n\n").map(|i| i + 2))
        .ok_or("no header separator")?;

    Ok(resp[body_start..].to_vec())
}

pub fn https_get(host: &str, path: &str, connect_timeout: u64, read_timeout: u64) -> Result<Vec<u8>, String> {
    let req = format!("GET {} HTTP/1.0\r\nHost: {}\r\nConnection: close\r\n\r\n", path, host);
    tls_roundtrip(host, req.as_bytes(), connect_timeout, read_timeout)
}

pub fn http_get(host: &str, path: &str, connect_timeout: u64, read_timeout: u64) -> Result<Vec<u8>, String> {
    use std::io::{Read, Write};
    let addr = (host, 80u16)
        .to_socket_addrs()
        .map_err(|e| e.to_string())?
        .next()
        .ok_or("no addr")?;
    let mut tcp = TcpStream::connect_timeout(&addr, Duration::from_secs(connect_timeout))
        .map_err(|e| e.to_string())?;
    tcp.set_read_timeout(Some(Duration::from_secs(read_timeout))).ok();
    let req = format!("GET {} HTTP/1.0\r\nHost: {}\r\nConnection: close\r\n\r\n", path, host);
    tcp.write_all(req.as_bytes()).map_err(|e| e.to_string())?;
    let mut resp = Vec::new();
    let _ = tcp.read_to_end(&mut resp);
    if resp.is_empty() { return Err("empty response".into()); }
    let body_start = resp.windows(4).position(|w| w == b"\r\n\r\n")
        .map(|i| i + 4)
        .or_else(|| resp.windows(2).position(|w| w == b"\n\n").map(|i| i + 2))
        .ok_or("no header separator")?;
    Ok(resp[body_start..].to_vec())
}

/// Multipart POST with a PNG attachment — used by the bug reporter.
pub fn https_post_multipart(
    host: &str, path: &str,
    category: &str, description: &str, png: &[u8],
    connect_timeout: u64, read_timeout: u64,
) -> Result<String, String> {
    let boundary = "MayhemBugBoundary9001";

    let mut body: Vec<u8> = Vec::new();
    // category field
    body.extend_from_slice(format!("--{boundary}\r\nContent-Disposition: form-data; name=\"category\"\r\n\r\n{category}\r\n").as_bytes());
    // description field
    body.extend_from_slice(format!("--{boundary}\r\nContent-Disposition: form-data; name=\"description\"\r\n\r\n{description}\r\n").as_bytes());
    // screenshot field
    body.extend_from_slice(format!("--{boundary}\r\nContent-Disposition: form-data; name=\"screenshot\"; filename=\"screenshot.png\"\r\nContent-Type: image/png\r\n\r\n").as_bytes());
    body.extend_from_slice(png);
    body.extend_from_slice(format!("\r\n--{boundary}--\r\n").as_bytes());

    let header = format!(
        "POST {} HTTP/1.0\r\nHost: {}\r\nContent-Type: multipart/form-data; boundary={}\r\nContent-Length: {}\r\nConnection: close\r\n\r\n",
        path, host, boundary, body.len()
    );
    let mut req = header.into_bytes();
    req.extend_from_slice(&body);

    let bytes = tls_roundtrip(host, &req, connect_timeout, read_timeout)?;
    String::from_utf8(bytes).map_err(|e| e.to_string())
}

pub fn https_post(host: &str, path: &str, body: &str, connect_timeout: u64, read_timeout: u64) -> Result<String, String> {
    let req = format!(
        "POST {} HTTP/1.0\r\nHost: {}\r\nContent-Type: application/json\r\nContent-Length: {}\r\nConnection: close\r\n\r\n{}",
        path, host, body.len(), body
    );
    let bytes = tls_roundtrip(host, req.as_bytes(), connect_timeout, read_timeout)?;
    String::from_utf8(bytes).map_err(|e| e.to_string())
}
